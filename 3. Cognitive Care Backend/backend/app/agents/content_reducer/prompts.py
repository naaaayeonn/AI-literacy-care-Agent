QUIZ_SYSTEM_PROMPT = """너는 독해 확인용 O/X 퀴즈 출제자다. 원문으로 참·거짓 판별이 가능한 진술문만 만든다."""

def build_quiz_prompt(summary: str, paragraph: str) -> str:
    """O/X 퀴즈 생성 사용자 프롬프트를 생성한다."""
    return f"""문단 요약: "{summary}"
원문 문단: "{paragraph}"
위 내용에 대한 O/X(참/거짓) 진술문 1개를 만들어라.
- answer 는 true/false 를 균등하게 섞어라.
- false일 때는 핵심 사실 하나만 그럴듯하게 왜곡한다(반대말·수치·인과 뒤집기).
- 주관·추측·본문에 없는 내용 금지.
JSON만 출력: {{"statement":"...", "answer": true|false, "explanation":"원문 근거 인용"}}"""


