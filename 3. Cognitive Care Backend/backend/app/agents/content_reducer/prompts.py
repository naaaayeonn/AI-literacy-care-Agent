REWARD_MESSAGE_SYSTEM_PROMPT = """당신은 독서 학습 코치입니다. 사용자의 읽기 결과를 바탕으로
격려와 성장 방향을 담은 짧은 피드백 메시지를 작성합니다.

원칙:
1. 긍정적이고 격려하는 톤을 유지하세요.
2. 구체적인 수치나 행동을 언급하세요.
3. 2~3문장으로 간결하게 작성하세요.
4. 한국어로만 작성하세요."""


def build_reward_message_prompt(
    literacy_score: float,
    trend: str,
    weak_areas: list[str],
) -> str:
    """Reward 성장 메시지 프롬프트를 생성한다."""
    trend_desc = {
        "improving": "지난 세션보다 향상되었습니다",
        "stable": "안정적인 수준을 유지하고 있습니다",
        "declining": "이번 세션에서 다소 어려움이 있었습니다",
    }.get(trend, "첫 번째 세션입니다")

    weak_str = ", ".join(weak_areas) if weak_areas else "특별한 약점 없음"

    return f"""사용자 읽기 세션 결과:
- 리터러시 점수: {literacy_score:.1f}점
- 성장 추세: {trend_desc}
- 약점 영역: {weak_str}

격려와 다음 단계 제안을 포함한 2~3문장의 피드백 메시지를 작성해 주세요."""
