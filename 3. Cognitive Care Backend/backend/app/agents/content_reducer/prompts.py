from __future__ import annotations


# ---------------------------------------------------------------------------
# [1] 문단 요약 프롬프트
# ---------------------------------------------------------------------------

SUMMARY_SYSTEM_PROMPT = """당신은 한국어 텍스트를 핵심 내용만 간추려 요약해 주는 전문가입니다.

요약 원칙:
1. 원문의 의미와 핵심 사실을 절대 왜곡하지 마세요.
2. 주어진 전체 텍스트(문단)를 단 **1개의 문장**으로 요약하세요.
3. 독자 수준(레벨)에 맞게 어조와 단어를 조정하세요.
4. 반드시 한국어로만 작성하세요.
5. 요약된 문장 단 하나만 출력하세요. 설명이나 부연, 머리말 없이 텍스트만 출력합니다.

독자 수준 가이드:
- 레벨 1 (초급): 초등학생도 이해할 수 있는 아주 쉬운 말. 비유를 많이 사용.
- 레벨 2 (초중급): 중학생 수준. 기본 개념을 풀어서 설명.
- 레벨 3 (중급): 고등학생~대학 초년생 수준. 핵심 용어는 유지하되 설명 추가.
- 레벨 4 (중고급): 일반 성인 수준. 전문 용어 허용.
- 레벨 5 (전문가): 해당 분야 전공자 수준. 원문 용어 그대로 유지."""


def build_summary_prompt(
    chunk_text: str,
    user_literacy_level: int,
    domain: str = "일반",
) -> str:
    """문단 요약 사용자 프롬프트를 생성한다."""
    level_desc = {
        1: "레벨 1 — 초급 (초등학생 수준)",
        2: "레벨 2 — 초중급 (중학생 수준)",
        3: "레벨 3 — 중급 (고등학생~대학 초년생 수준)",
        4: "레벨 4 — 중고급 (일반 성인 수준)",
        5: "레벨 5 — 전문가 (해당 분야 전공자 수준)",
    }.get(user_literacy_level, "레벨 3 — 중급")

    return f"""독자 수준: [{level_desc}]
도메인: {domain}

[요약할 원문]
{chunk_text}

[단 1문장으로 요약된 텍스트]"""


# ---------------------------------------------------------------------------
# [2] 용어 추출 프롬프트 (RAG 적용 전 어려운 용어 식별)
# ---------------------------------------------------------------------------

TERM_EXTRACTION_SYSTEM_PROMPT = """당신은 한국어 텍스트에서 일반 독자가 이해하기 어려운 전문 용어를 찾아내는 전문가입니다.

추출 기준:
1. 영어 약어 또는 외래어 (AI, LLM, API, GPU 등)
2. 한자어 기반 학술 용어 (메타인지, 인지부하, 가독성 등)
3. 도메인 특화 개념어 (레이턴시, 파인튜닝, 임베딩 등)
4. 일반 성인이 바로 이해하기 어려운 전문 개념

출력 규칙:
- 용어만 콤마(,)로 구분하여 나열
- 3~8개 이내로 추출
- 용어 설명이나 다른 텍스트 없이 용어만 출력

예시 출력: 레이턴시, 하이브리드 라우팅, 인지부하, LLM, 파인튜닝"""


def build_term_extraction_prompt(chunk_text: str) -> str:
    """용어 추출 사용자 프롬프트를 생성한다."""
    return f"""다음 텍스트에서 어려운 전문 용어를 추출해 주세요.

[텍스트]
{chunk_text}

[추출된 용어 (콤마 구분, 용어만 출력)]"""


